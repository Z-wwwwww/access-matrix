-- V15: demo 系データの日本語化 & 内蔵フラグ解除
-- ============================================
-- 目的：
--   1) demo 用 5 ロール (ROLE11-15) を業務担当者が UI から削除できるよう、
--      is_built_in を 0 に戻す（デモ確認後にお掃除可能にする）
--   2) demo ロール名 / 説明 / 部署名を業務担当者に分かるよう日本語化
--
-- 影響なし：role の code は変更しない（コード側の参照と整合）。
-- 冪等：CASE WHEN で id ベースに振り分け。

UPDATE core_rbac_role
   SET is_built_in = 0,
       name = CASE id
                WHEN '00000000000000000000ROLE11' THEN 'デモ：全範囲'
                WHEN '00000000000000000000ROLE12' THEN 'デモ：自部署＋下位'
                WHEN '00000000000000000000ROLE13' THEN 'デモ：自部署のみ'
                WHEN '00000000000000000000ROLE14' THEN 'デモ：本人のみ'
                WHEN '00000000000000000000ROLE15' THEN 'デモ：カスタム部署'
              END,
       description = CASE id
                       WHEN '00000000000000000000ROLE11' THEN 'テナント内すべてのタスクを閲覧'
                       WHEN '00000000000000000000ROLE12' THEN '自部署とその下位部署すべてのタスクを閲覧'
                       WHEN '00000000000000000000ROLE13' THEN '自部署のみのタスクを閲覧'
                       WHEN '00000000000000000000ROLE14' THEN '自分が作成したタスクのみ閲覧'
                       WHEN '00000000000000000000ROLE15' THEN '明示的にバインドした部署（京都支社）を閲覧'
                     END,
       update_time = CURRENT_TIMESTAMP
 WHERE id IN (
        '00000000000000000000ROLE11',
        '00000000000000000000ROLE12',
        '00000000000000000000ROLE13',
        '00000000000000000000ROLE14',
        '00000000000000000000ROLE15'
   );

-- 部署名も日本語化（demo データと一貫させ、本社/支社の英日混在を回避）
UPDATE core_rbac_dept
   SET name = CASE id
                WHEN '00000000000000000000DEPT01' THEN '本社'
                WHEN '00000000000000000000DEPT02' THEN '東京支社'
                WHEN '00000000000000000000DEPT03' THEN '大阪支社'
                WHEN '00000000000000000000DEPT04' THEN '京都支社'
              END,
       update_time = CURRENT_TIMESTAMP
 WHERE id IN (
        '00000000000000000000DEPT01',
        '00000000000000000000DEPT02',
        '00000000000000000000DEPT03',
        '00000000000000000000DEPT04'
   );
